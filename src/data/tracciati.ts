export function getZonaAttiva(tracciato: Tracciato, valore: number): ZonaTracciato {
  return tracciato.zone.find(z => valore >= z.da && valore <= z.a) ?? tracciato.zone[0];
}

export function isGameOver(tracciato: Tracciato, valore: number): boolean {
  if (tracciato.id === 'TNI' && valore === 15) return true;
  if (tracciato.id === 'DEFCON' && valore === 1) return true;
  return false;
}
