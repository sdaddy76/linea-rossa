export function getObiettiviPerFazione(faction: ObjFazione): ObiettivoSegreto[] {
  return TUTTI_GLI_OBIETTIVI.filter(o => o.faction === faction && o.attivo);
}

// ─── assignObjectives ─────────────────────────────────────
// Assegna num_draw obiettivi segreti casuali (no ripetizioni) per una fazione.
// Usare a inizio partita per ogni giocatore.
export function assignObjectives(
  faction: ObjFazione,
  numDraw = 3,
  excludeIds: string[] = [],
): ObiettivoSegreto[] {
  const pool = getObiettiviPerFazione(faction)
    .filter(o => !excludeIds.includes(o.obj_id));
  // Fisher-Yates shuffle
  const arr = [...pool];
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr.slice(0, Math.min(numDraw, arr.length));
}

// ─── evalObjectiveCondition ───────────────────────────────
// Valuta automaticamente un obiettivo con condizione 'tracciato'.
// gs = sottoinsieme di GameState. Ritorna true/false/null (null = manuale).
export function evalObjectiveCondition(
  obj: ObiettivoSegreto,
  gs: Partial<Record<string, number>>,
): boolean | null {
  if (!obj.condizione_tipo || obj.condizione_tipo !== 'tracciato') return null;
  if (!obj.condizione_campo || !obj.condizione_op || obj.condizione_valore == null) return null;
  const val = gs[obj.condizione_campo];
  if (val === undefined || val === null) return null;
  switch (obj.condizione_op) {
    case '>=': return val >= obj.condizione_valore;
    case '<=': return val <= obj.condizione_valore;
    case '==': return val === obj.condizione_valore;
    default:   return null;
  }
}

