// Pesca casuale un evento (usato all'inizio del turno)
export function pescaEvento(eventiUsati: string[] = []): EventoCard {
  const disponibili = TUTTI_GLI_EVENTI.filter(e => !eventiUsati.includes(e.event_id));
  const pool = disponibili.length > 0 ? disponibili : TUTTI_GLI_EVENTI; // ricomincia se esauriti
  return pool[Math.floor(Math.random() * pool.length)];
}

// Ottieni evento per ID
export function getEventoById(id: string): EventoCard | undefined {
  return TUTTI_GLI_EVENTI.find(e => e.event_id === id);
}

